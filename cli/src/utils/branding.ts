/**
 * Black terminal compatible info box
 */
export function blackTerminalInfoBox(
  title: string,
  content: string[],
  type: "info" | "warning" | "error" = "info",
): string {
  if (!blackTerminalMode) {
    return infoBox(title, content, type);
  }
  
  const colors = BLACK_TERMINAL_COLORS;
  const color = colors[type];
  const icon = ICONS[type];

  const maxWidth = Math.max(title.length, ...content.map((line) => line.length)) + 4;
  const border = "‚îÄ".repeat(maxWidth);

  let box = `${color("‚îå" + border + "‚îê")}\n`;
  box += `${color("‚îÇ")} ${icon} ${chalk.white.bold(title)}${" ".repeat(maxWidth - title.length - 2)} ${color("‚îÇ")}\n`;
  box += `${color("‚îú" + border + "‚î§")}\n`;

  content.forEach((line) => {
    box += `${color("‚îÇ")} ${chalk.white(line)}${" ".repeat(maxWidth - line.length - 1)} ${color("‚îÇ")}\n`;
  });

  box += `${color("‚îî" + border + "‚îò")}`;

  return box;
}
